a = 9
b = 2

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
exponenciacao = a ** b
resto = a % b

print(f"{a} + {b} = {soma}")
print(f"{a} - {b} = {subtracao}")
print(f"{a} * {b} = {multiplicacao}")
print(f"{a} / {b} = {divisao}")
print(f"{a} ** {b} = {exponenciacao}")
print(f"{a} % {b} = {resto}")